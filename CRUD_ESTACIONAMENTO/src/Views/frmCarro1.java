        String descricao = (String)tblConsulta.getValueAt(linha, 3);
        String entrada = (String)tblConsulta.getValueAt(linha, 4);
        String saida = (String)tblConsulta.getValueAt(linha, 5);
        String valor = (String)tblConsulta.getValueAt(linha, 6);
        
