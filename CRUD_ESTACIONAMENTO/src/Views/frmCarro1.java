    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:

        CarroBean cb = new CarroBean();
        CarroDAO cd = new CarroDAO();

        cb.setIdPlaca(Integer.valueOf(txtIdPlaca.getText()));
        Object[] opcoes = { "Sim", "Não" };

        int i = JOptionPane.showOptionDialog(null,"Deseja excluir esse veículo:"+txtIdPlaca.getText()+"?","Exclusão",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE, null,opcoes, opcoes[0]);

        if (i == JOptionPane.YES_OPTION) {
            lblMenssagem.setText(cd.excluir(cb));
        }
        CarroDAO dao = new CarroDAO();
        dao.excluir(cb);

    }//GEN-LAST:event_btnExcluirMouseClicked

